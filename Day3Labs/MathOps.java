public class MathOps{
	public static void main(String[] args){
		int x=5;
		int y=3;
		int z;

		z=x+y;
		System.out.println(z);

		z=x-y;
		System.out.println(z);

		z=x*y;
		System.out.println(z);

		z=x/y;
		System.out.println(z);

		z=x%y;
		System.out.println(z);

		float f=3.6f;
		int i=22;
		int res=(int)f +i;
		System.out.println(res);

		f=3.6f;
		i=22;
		res=(int)f +i;
		System.out.println(res);
	}
}